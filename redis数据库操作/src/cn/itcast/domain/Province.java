package cn.itcast.domain;

public class Province {
    /**
     * 实体类，对应着数据库的表
     */
    private int id;
    private  String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
